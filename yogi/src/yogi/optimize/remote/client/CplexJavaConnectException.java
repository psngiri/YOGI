package yogi.optimize.remote.client;

public class CplexJavaConnectException extends Exception {

	private static final long serialVersionUID = -5127449075022315021L;

	public CplexJavaConnectException() {
		super();
	}

	public CplexJavaConnectException(String message, Throwable cause) {
		super(message, cause);
	}

	public CplexJavaConnectException(String message) {
		super(message);
	}

	public CplexJavaConnectException(Throwable cause) {
		super(cause);
	}
}
